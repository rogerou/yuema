package com.exam.date;

public class MyGouda {

	private String activity;
	private String condation;

	public MyGouda(String activity, String condation) {
		super();
		this.activity = activity;
		this.condation = condation;
	}

	public String getActivity() {
		return activity;
	}

	public void setActivity(String activity) {
		this.activity = activity;
	}

	public String getCondation() {
		return condation;
	}

	public void setCondation(String condation) {
		this.condation = condation;
	}

}
